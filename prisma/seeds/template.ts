export default async function seedTemplates(prisma: PrismaClient) {
  console.log("Seeding system templates...");

  // 既存のシステムテンプレート（userId = undefined）を削除
  await prisma.appTemplate.deleteMany({
    where: {
      userId: undefined,
    },
  });

  // テンプレートを作成（userId = undefinedでシステムテンプレート）
  const templates = await Promise.all(
    TEMPLATES.map((template) =>
      prisma.appTemplate.create({
        data: {
          name: template.name,
          body: template.body,
        },
      }),
    ),
  );

  console.log(`Seeded ${templates.length} system templates`);
  return templates;
}
